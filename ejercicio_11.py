# Pseudocodigo 
# Solicitar la nota del usuario y guardarla en una variable llamada “nota_usuario”
# Si la variable “nota_usuario” es mayor de 70:
#             Imprimir por consola el texto “Aprovado”
# Caso contrario:
#             Imprimir por consola el texto “Desaprovado”


# Codigo

nota_usuario = int(input("Ingrese un nota: "))

if(nota_usuario > 70) :
    print("“Aprovado”")
else:
    print("Desaprovado")