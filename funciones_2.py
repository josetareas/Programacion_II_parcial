


def suma(numero1:int, numero2:int): 
    suma = numero1 + numero2
    return suma



print(suma(5,3))

def resta(numero1:int, numero2:int): 
    resta = numero1 - numero2
    return resta



print(resta(11,6))

def multiplicion(numero1:int, numero2:int): 
    multiplicion = numero1 * numero2
    return multiplicion



print(multiplicion(8,3))

def division(numero1:int, numero2:int) -> float:
    division = numero1 / numero2
    return division
    


print(division(6,3))